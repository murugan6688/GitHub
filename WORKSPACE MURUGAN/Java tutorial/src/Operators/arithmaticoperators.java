package Operators;
public class arithmaticoperators 
{
	public static void main(String[] args)
	{
		int a=90,b=100,c=200;
		int e=a+b;
		int f=a-c;
		int g = a*b;
		double h = b / a;
		double i = b % a;
		System.out.println("value of i after a%b is " +i);	
		i++;
		System.out.println(+i);
		i--;
		System.out.println(+i);
		System.out.println("Value of e after a+b is " +e);
		System.out.println("value of f after a-c is " +f);
		System.out.println("value of g after a*b is " +g);	
		System.out.println("value of h after a/b is " +h);	

	}
}